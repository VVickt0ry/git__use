# 8. 删除文件的情况

# 适合删除远程仓库中已存在的旧版本文件A
# 步骤1：在Git Bash中进入本地仓库目录
# 步骤2：删除本地的文件A: rm 文件名A  
# 步骤3：将删除文件A的操作标记为暂存,即使文件被删除，仍需add标记删除操作
# 步骤4：提交删除操作到本地版本库（备注清晰，方便后续追溯）
# 步骤5：推送到远程仓库，让远程同步删除文件A


# 适合本地需要留A做备份，只清理远程仓库的情况
# 步骤1：仅移除Git对文件A的追踪: git rm --cached 文件名A  
# 步骤2：提交“移除追踪”的操作
# 步骤3：推送到远程，远程的A会被删除

# Git的删除本质是提交一个‘删除动作’并同步到远程
# 删错可通过git checkout 版本号 -- 文件名 恢复


