# 5. 有关分支和仓库的概念

# 仓库是 Git 管理代码的核心载体，分为本地仓库和远程仓库，二者通过Git命令同步
# 本地仓库：你电脑里的代码文件夹，执行git init后成为Git可管理的仓库
# 远程仓库：GitHub/GitLab 上的云端仓库，是本地仓库的云端备份，方便协作和代码托管
# origin：Git给远程仓库的默认昵称
# 远程快照：本地仓库里会保存远程分支的快照，代表本地记录的远程分支最新状态




# 分支的本质是一种指针指向仓库的提交记录，核心价值是隔离开发，保证主分支稳定
# git会自动创建的master分支，master分支是默认的主分支，所有的代码修改都会提交到master分支上
# HEAD指针：特殊指针，指向你当前正在操作的分支（HEAD -> master表示正在本地master分支工作）
# 每次执行git commit，当前分支的指针会自动移动到新的提交记录

# 主分支（main/master）	核心分支，保存经过测试的稳定代码，远程仓库默认显示这个分支
# 开发分支（dev/develop）	从主分支复制，保存开发中的代码
# 功能分支（feature-xxx）	从开发分支复制，专门开发单个新功能，完成后合并回开发分支
# BUG 分支（bugfix-xxx）	从主分支复制，专门修复线上 Bug，完成后合并回主分支
# 发布分支（release-xxx）	从开发分支复制，准备发布版本，完成后合并回主分支+开发分支



# 切换分支与合并分支

# 切换分支前必须提交，如果当前分支有未提交的变更，切换分支会把这些变更带过去，导致分支污染
# 合并分支前，先执行git pull origin <分支名>拉取最新内容，避免合并过时的代码
# 合并完成的功能分支/BUG分支，及时删除，避免分支过多混乱

# 想合并dev分支的话，需要先在develop分支上，把文件先提交到暂存区，再提交代码文件
# 之后再切换回master分支，再使用命令进行合并





# 分支合并

# 切换到开发分支，提交未完成修改，避免分支污染
# 拉取远程开发分支最新内容，避免合并过时代码
# 切换到主分支，拉取远程主分支最新内容
# 合并开发分支到主分支
# 推送合并后的主分支到远程






