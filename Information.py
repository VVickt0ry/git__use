# 6. 远程仓库与本地仓库的同步

# 核心目标是让本地当前分支的内容，和远程仓库对应的分支完全一致
# 远程删了文件A、新增了文件B → 执行git pull后，本地也会删A、加B
# 远程修改了文件C的内容 →执行git pull后，本地C的内容也会变成远程的最新版本
# 本地删了A并推到远程 →执行git pull能验证本地的删除操作是否同步到远程，也能拉取远程的其他变更
# git pull其实是两个命令的组合，不是单纯拉代码

# git fetch
# 下载但不合并：把远程仓库的最新内容，下载到本地的远程快照区，但不修改你正在用的本地main分支
# git merge
# 合并到本地：把远程快照区的最新内容，合并到你正在用的本地main分支，让本地分支和快照区一致



# 执行git pull有两个关键作用
# 如果远程A已经被删，git pull会提示Already up to date. 说明本地和远程一致
# 如果队友同时修改了远程的B文件，git pull会把这些修改拉到本地避免你本地的B和远程的B版本不一致
# 确保你后续执行git status时，能看到准确的状态，而不是误提示本地有未推送的变更


# 执行前先提交本地修改：如果本地有未提交的修改
# git pull可能触发合并冲突，Git不知道该保留你的修改还是远程的修改
# 可先git add . → git commit -m "备注"，再执行git pull

# 如果想先看远程有哪些变更，再决定是否合并，可先执行git fetch
# 查看git log origin/main，确认没问题后再执行git merge


