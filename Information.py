# 9. 误删文件的情况

# 可通过Git版本记录恢复，前提是A曾经被提交过
# 步骤1：查看提交记录，找到删除A之前的版本号
# git log --oneline  # 输出类似：e5ea35a 删除旧版本文件A...  a6d5e0b 提交文件A...
# 步骤2：恢复文件A: git checkout 版本号 -- 文件名A
# 步骤3：重新推送恢复后的A到远程


# 团队协作场景：告知队友
# 如果有其他人协作这个仓库，建议告知他们执行git pull，同步远程删除 A 的操作，避免他们本地的A和远程冲突
# 如果A是废弃文件，不想再误提交，可把A加入 .gitignore
# 步骤1: .gitignore，添加文件A的名称,echo "文件名A" >> .gitignore
# 步骤2: 提交.gitignore的修改
# git add .gitignore
# git commit -m ".gitignore中忽略废弃文件A"
# git push


